    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure to delete this user?", "Delete Book", dialogButton);
        if(dialogResult == 0) {
            try{
                String user = jComboBox1.getSelectedItem().toString();
                if(!user.equals("")) {
                    String query = "DELETE FROM admin WHERE username = ?";
                    pst = conn.prepareStatement(query);
                    pst.setString(1, user);
                    pst.execute();
                    pst.close();
                    JOptionPane.showMessageDialog(null, user + " has been deleted!");
                    pst.close();
                    getData();
                } else {
                    JOptionPane.showMessageDialog(null, "Error! There's something wrong.");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error! Put a username in Combobox!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
 
