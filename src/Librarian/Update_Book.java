    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        try {
            Statement stmt = conn.createStatement();
            String combobox = jComboBox1.getSelectedItem().toString();
            String sql = "select * from books where Title = '"+combobox+"'";
            rs = stmt.executeQuery(sql);
            if (rs.next()) {  
                jTextField2.setText(rs.getString("Title"));
                jTextField3.setText(rs.getString("Author"));
                jTextField4.setText(rs.getString("Edition"));
                jTextField5.setText(rs.getString("Publication"));
                jTextField6.setText(rs.getString("ISBN"));
                jSpinner1.setValue(rs.getInt("Quantity"));
          
            }
            stmt.close();
            rs.close();
        } catch (Exception e) {  
            JOptionPane.showMessageDialog(null,"Failed to Connect to Database2","Error Connection", JOptionPane.WARNING_MESSAGE);  
            System.exit(0);  
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

