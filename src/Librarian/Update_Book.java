    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         
        
        try{
            String combobox = jComboBox1.getSelectedItem().toString();
            String title = jTextField2.getText();
            String author = jTextField3.getText(); 
            String edition = jTextField4.getText(); 
            String publication = jTextField5.getText(); 
            String isbn = jTextField6.getText(); 
            int quantity = (int) jSpinner1.getValue();
            
            if(!title.equals("") && !author.equals("") && !edition.equals("") && !publication.equals("") && !isbn.equals("")) {
                String query = "update books set title=?,author=?,edition=?,publication=?,isbn=?,quantity=? where title = ?";
                pst = conn.prepareStatement(query);
                pst.setString(1, title);
                pst.setString(2, author);
                pst.setString(3, edition);
                pst.setString(4, publication);
                pst.setString(5, isbn);
                pst.setInt(6, quantity);
                pst.setString(7, combobox);
                pst.executeUpdate();
                pst.close();
                JOptionPane.showMessageDialog(null, title + " has been updated!");
              
                pst.close();
            } else {
                JOptionPane.showMessageDialog(null, "Error! There's something wrong.");
            }
            getData();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

